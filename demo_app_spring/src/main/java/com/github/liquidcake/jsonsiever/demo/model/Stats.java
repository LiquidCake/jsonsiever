package com.github.liquidcake.jsonsiever.demo.model;

public class Stats {
    private int str;
    private int agi;

    public Stats(int str, int agi) {
        this.str = str;
        this.agi = agi;
    }

    public int getStr() {
        return str;
    }

    public void setStr(int str) {
        this.str = str;
    }

    public int getAgi() {
        return agi;
    }

    public void setAgi(int agi) {
        this.agi = agi;
    }
}
